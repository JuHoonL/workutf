package com.biz.test_1105;

public class RelationalOp {
	public static void main (String [] args) { 
		System.out.println("3 >= 2 : " + (3 >= 2));		// 항상 =와 복합연산자를 사용할때는 =가 나중에 나와야한다.
		System.out.println("3 <= 2 : " + (3 <= 2));
		System.out.println("7.0 == 7 : " + (7.0 == 7));
		System.out.println("7.0 != 7 : " + (7.0 != 7)); // != 는 다른가를 물어보는 명령어이다.
	}
}
